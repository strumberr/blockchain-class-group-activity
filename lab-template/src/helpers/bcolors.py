class bcolors:
    # Colors for the terminal output
    OK = "\033[92m"
    WARNING = "\033[93m"
    ERROR = "\033[91m"
    RESET = "\033[0m"

    # Base colors
    BLACK = "\033[30m"
    RED = "\033[31m"
    GREEN = "\033[32m"
    YELLOW = "\033[33m"
    BLUE = "\033[34m"
    MAGENTA = "\033[35m"
    CYAN = "\033[36m"
    WHITE = "\033[37m"

    # Bold colors
    BOLD_BLACK = "\033[1;30m"
    BOLD_RED = "\033[1;31m"
    BOLD_GREEN = "\033[1;32m"
    BOLD_YELLOW = "\033[1;33m"
    BOLD_BLUE = "\033[1;34m"
    BOLD_MAGENTA = "\033[1;35m"
    BOLD_CYAN = "\033[1;36m"
    BOLD_WHITE = "\033[1;37m"

    # Background colors
    BG_BLACK = "\033[40m"
    BG_RED = "\033[41m"
    BG_GREEN = "\033[42m"
    BG_YELLOW = "\033[43m"
    BG_BLUE = "\033[44m"
    BG_MAGENTA = "\033[45m"
    BG_CYAN = "\033[46m"
    BG_WHITE = "\033[47m"

    # Bold background colors
    BG_BOLD_BLACK = "\033[1;40m"
    BG_BOLD_RED = "\033[1;41m"
    BG_BOLD_GREEN = "\033[1;42m"
    BG_BOLD_YELLOW = "\033[1;43m"
    BG_BOLD_BLUE = "\033[1;44m"
    BG_BOLD_MAGENTA = "\033[1;45m"
    BG_BOLD_CYAN = "\033[1;46m"
    BG_BOLD_WHITE = "\033[1;47m"

    # Special formatting
    UNDERLINE = "\033[4m"
    END = "\033[0m"
